//图通常有什么特点呢?
  //一组顶点:通常用V(Vertex)表示顶点的集合
  //一组边:通常用E(Edge)表示边的集合
    //边是顶点和顶点之间的连线
    //边可以是有向的,也可以是无向的
    //比如A --- B ,通常表示无向.A --> B,通常表示有向
//图的一些常见术语
  //顶点---表示图中的一个节点
  //边---顶点与顶点之间的连线
  //度---一个顶点的度是相邻顶点的数量
  //路径---顶点V1、V2、...、Vn的一个连续序列
  //......
//邻接矩阵的表示
  //邻接矩阵让每个节点和一个整数相关联,该整数作为数组下标值
  //我们用一个二维数组来表示顶点之间的连接
  //能够比较友好地表达有向图，在二维数组中很快地找到一个顶点和哪些顶点有连线
  //另外,A-A(自己连接自己的线),通常使用0表示
  //
  //邻接矩阵的问题:
  //如果图是一个稀疏图，就会很浪费计算机的存储空间